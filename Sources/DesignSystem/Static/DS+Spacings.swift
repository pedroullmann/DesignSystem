import Foundation

public extension DesignSystem {
    struct Spacings {
        /// Extra small spacing, 4 points.
        public static let xSmall: CGFloat = 4

        /// Small spacing, 8 points.
        public static let small: CGFloat = 8

        /// Medium spacing, 16 points.
        public static let medium: CGFloat = 16

        /// Large spacing, 24 points.
        public static let large: CGFloat = 24

        /// Extra large spacing, 32 points.
        public static let xLarge: CGFloat = 32

        /// Double extra large spacing, 64 points.
        public static let xxLarge: CGFloat = 64

        /// Triple extra large spacing, 128 points.
        public static let xxxLarge: CGFloat = 128
    }
}
