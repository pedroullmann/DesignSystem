import Foundation

public extension DesignSystem {
    struct CornerRadius {
        /// Radius for extra small rounded corners, 4 points.
        public static let xSmall: CGFloat = 4

        /// Radius for small rounded corners, 8 points.
        public static let small: CGFloat = 8

        /// Radius for medium rounded corners, 16 points.
        public static let medium: CGFloat = 16

        /// Radius for large rounded corners, 24 points.
        public static let large: CGFloat = 24
    }
}
